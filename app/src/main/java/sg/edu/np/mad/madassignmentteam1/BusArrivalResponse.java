public class BusArrivalResponse {
    private String busStopCode;
    private String serviceNo;
    private String nextBus;
    private String nextBus2;
    private String nextBus3;

    public String getBusStopCode() {
        return busStopCode;
    }

    public void setBusStopCode(String busStopCode) {
        this.busStopCode = busStopCode;
    }

    public String getServiceNo() {
        return serviceNo;
    }

    public void setServiceNo(String serviceNo) {
        this.serviceNo = serviceNo;
    }

    public String getNextBus() {
        return nextBus;
    }

    public void setNextBus(String nextBus) {
        this.nextBus = nextBus;
    }

    public String getNextBus2() {
        return nextBus2;
    }

    public void setNextBus2(String nextBus2) {
        this.nextBus2 = nextBus2;
    }

    public String getNextBus3() {
        return nextBus3;
    }

    public void setNextBus3(String nextBus3) {
        this.nextBus3 = nextBus3;
    }
}

